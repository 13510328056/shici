class RhythmCheckService:
    """格律校验服务"""

    async def check(
        self,
        content: str,
        genre: str = '七绝',
        rhyme_system: str = '平水韵',
    ) -> dict:
        """校验格律"""
        lines = [l.strip() for l in content.replace('，', '').replace('。', '').replace('\n', '').split() if l.strip()]
        lines = [l for l in lines if l and not all(c in '，。！？、；：""''（）' for c in l)]

        # 如果包含标点，去除
        import re
        lines = [re.sub(r'[，。！？、；：""（）]', '', l) for l in lines if l.strip()]

        expected_lines, chars_per_line = GENRE_PATTERNS.get(genre, (4, 7))

        errors = []
        char_details = []

        for i, line in enumerate(lines):
            if i >= expected_lines:
                break
            line_detail = []
            for j, ch in enumerate(line):
                pz = get_pingze(ch)
                rhyme = get_rhyme_group(ch)
                line_detail.append({
                    'char': ch,
                    'position': j + 1,
                    'pingze': '平' if pz == 1 else ('仄' if pz == -1 else ('可平可仄' if pz == 0 else '未知')),
                    'rhyme_group': rhyme or '',
                })
            char_details.append({'line': i + 1, 'chars': line_detail, 'text': line})

        # 检测韵脚（律诗/绝句押韵规则：偶数句押韵，首句可押可不押）
        if rhyme_system == '平水韵' and len(lines) >= 2:
            rhyme_lines = [i for i in range(1, len(lines), 2)]  # 偶数句（2,4,6,8句）
            if expected_lines >= 4:
                # 首句也检查押韵
                first_line_last = lines[0][-1] if lines[0] else ''
                second_line_last = lines[1][-1] if len(lines) > 1 else ''
                if second_line_last:
                    sg = get_rhyme_group(second_line_last)
                    if not sg:
                        errors.append({
                            'type': 'rhyme_unknown',
                            'position': f'第2句末"{second_line_last}"',
                            'message': f'未知韵部: {second_line_last}',
                        })
                    # 检查其他偶数句
                    for ri in range(2, len(lines), 2):
                        if ri < len(lines):
                            last_ch = lines[ri][-1]
                            other_sg = get_rhyme_group(last_ch)
                            if other_sg and sg and other_sg != sg:
                                errors.append({
                                    'type': 'out_of_rhyme',
                                    'position': f'第{ri+1}句末"{last_ch}"',
                                    'expected_rhyme': sg,
                                    'actual_rhyme': other_sg,
                                    'message': f'出韵："{last_ch}"属{other_sg}，应押{sg}韵',
                                })

        # 检测平仄
        if genre in ('七绝', '七律', '五绝', '五律') and len(lines) >= 4:
            pattern = PATTERN_QILV_PING if len(lines) >= 2 and get_pingze(lines[0][0]) == 1 else PATTERN_QILV_ZE
            for i, line in enumerate(lines):
                if i >= len(pattern):
                    break
                p_line = pattern[i]
                # 根据体裁调整：五言取后5位，七言全取
                pattern_chars = p_line[-chars_per_line:] if chars_per_line == 5 else p_line[:chars_per_line]
                for j, ch in enumerate(line):
                    if j >= len(pattern_chars):
                        break
                    expected_pz = pattern_chars[j]
                    if expected_pz in ('—', '|'):
                        actual_pz = get_pingze(ch)
                        if actual_pz is not None and actual_pz != 0:  # 排除可平可仄和未知
                            expected = '平' if expected_pz == '—' else '仄'
                            actual = '平' if actual_pz == 1 else '仄'
                            if expected != actual:
                                errors.append({
                                    'type': 'tone_error',
                                    'line': i + 1,
                                    'position': j + 1,
                                    'char': ch,
                                    'expected': expected,
                                    'actual': actual,
                                    'message': f'第{i+1}句第{j+1}字"{ch}"应为{expected}声，实为{actual}声',
                                })

        # 检测失粘失对
        stick_errors = self._check_stick_and_couple(lines, chars_per_line)
        errors.extend(stick_errors)

        return {
            'content': content,
            'genre': genre,
            'rhyme_system': rhyme_system,
            'line_count': min(len(lines), expected_lines),
            'expected_lines': expected_lines,
            'passed': len(errors) == 0,
            'errors': errors,
            'details': char_details,
        }

    def _check_stick_and_couple(self, lines: list[str], chars_per_line: int) -> list[dict]:
        """检测失粘（stick）和失对（couple）"""
        errors = []
        if len(lines) < 4:
            return errors

        for i in range(0, len(lines) - 1, 2):
            if i + 1 >= len(lines):
                break
            # 对：同一联内上下句第2字平仄应相反
            if len(lines[i]) > 1 and len(lines[i + 1]) > 1:
                pz1 = get_pingze(lines[i][1])
                pz2 = get_pingze(lines[i + 1][1])
                if pz1 is not None and pz2 is not None and pz1 != 0 and pz2 != 0:
                    if pz1 == pz2:
                        errors.append({
                            'type': 'no_couple',
                            'position': f'第{i+1}联',
                            'message': f'失对：第{i+1}句第2字与第{i+2}句第2字平仄相同',
                        })

        for i in range(1, len(lines) - 2, 2):
            if i + 1 >= len(lines):
                break
            # 粘：上联对句与下联出句第2字平仄应相同
            if len(lines[i]) > 1 and len(lines[i + 1]) > 1:
                pz1 = get_pingze(lines[i][1])
                pz2 = get_pingze(lines[i + 1][1])
                if pz1 is not None and pz2 is not None and pz1 != 0 and pz2 != 0:
                    if pz1 != pz2:
                        errors.append({
                            'type': 'no_stick',
                            'position': f'第{i+1}/{i+2}句',
                            'message': f'失粘：第{i+1}句第2字与第{i+2}句第2字平仄不同',
                        })
        return errors
